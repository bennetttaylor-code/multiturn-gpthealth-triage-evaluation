#!/usr/bin/env python3
"""Write results/figures/figure_index.txt, summarizing the 12 figures/tables
produced by the individual fig*.py / figS*.py / table1_descriptives.py /
extended_data_fig1_study_design.py scripts.

Run this after all figure scripts have completed — it checks for the
expected output files on disk rather than tracking status in-process, since
each figure is now generated by its own independent script/subprocess.

Usage:
    python write_figure_index.py --output <fig_dir>
"""
import argparse
import os

parser = argparse.ArgumentParser(description='Write the figure index summary')
parser.add_argument('--output', required=True, help='Figures directory to scan and write the index into')
args = parser.parse_args()

FIG_DIR = args.output

EXPECTED = [
    'fig1_direction_by_dataset',
    'fig2_dist_comparisons',
    'fig3_cc_concordance',
    'fig4_promptcount_multiturn',
    'table1_descriptives',
    'extended_data_fig1_study_design',
    'figS1_confusion_matrices',
    'figS2_sensitivity_forest',
    'figS3_triage_direction_by_level',
    'figS4_promptcount',
    'figS5_complaint_heatmap',
    'figS6_cc_vs_nurse_confusion',
]


def status_for(name):
    png_ok = os.path.isfile(os.path.join(FIG_DIR, f'{name}.png'))
    pdf_ok = os.path.isfile(os.path.join(FIG_DIR, f'{name}.pdf'))
    if png_ok and pdf_ok: return 'OK'
    missing = [ext for ext, ok in [('png', png_ok), ('pdf', pdf_ok)] if not ok]
    return f"SKIPPED: missing {', '.join(missing)}"


index_lines = [
    "FIGURE INDEX — Triage Analysis Publication Figures",
    "=" * 60,
    "",
    "MAIN FIGURES",
    "-" * 40,
    "fig1_direction_by_dataset.png/pdf",
    "  Figure 1: Under-triage / correct / over-triage by interaction condition and dataset.",
    "fig2_dist_comparisons.png/pdf",
    "  Figure 2: Clinical distance-score distributions vs. clinician-adjudicated and nurse triage.",
    "fig3_cc_concordance.png/pdf",
    "  Figure 3: Concordance between clinician-adjudicated and nurse-triage standards",
    "  (distribution of category differences).",
    "fig4_promptcount_multiturn.png/pdf",
    "  Figure 4: Multiturn prompt count vs. clinical distance from nurse triage (Spearman).",
    "",
    "TABLE",
    "-" * 40,
    "table1_descriptives.png/pdf",
    "  Table 1: Case characteristics by dataset (booktabs style).",
    "",
    "EXTENDED DATA",
    "-" * 40,
    "extended_data_fig1_study_design.png/pdf",
    "  Extended Data Figure 1: Study design and analysis workflow.",
    "",
    "SUPPLEMENTARY FIGURES",
    "-" * 40,
    "figS1_confusion_matrices.png/pdf",
    "  Supplementary Figure 1: 4×4 confusion matrices, Natural & Multiturn vs. both references.",
    "figS2_sensitivity_forest.png/pdf",
    "  Supplementary Figure 2: Sensitivity forest (primary/conservative/aggressive resolution).",
    "figS3_triage_direction_by_level.png/pdf",
    "  Supplementary Figure 3: Under/correct/over stratified by reference triage level (A–D).",
    "figS4_promptcount.png/pdf",
    "  Supplementary Figure 4: Prompt count vs. distance scatter (all conditions × references).",
    "figS5_complaint_heatmap.png/pdf",
    "  Supplementary Figure 5: Performance heatmap by chief-complaint category.",
    "figS6_cc_vs_nurse_confusion.png/pdf",
    "  Supplementary Figure 6: Clinician-adjudicated vs. nurse-triage agreement matrix.",
    "",
    "=" * 60,
    "RENDER STATUS:",
]
for name in EXPECTED:
    index_lines.append(f"  {name}: {status_for(name)}")

idx_path = os.path.join(FIG_DIR, 'figure_index.txt')
with open(idx_path, 'w') as f:
    f.write('\n'.join(index_lines))
print(f"Figure index saved: {idx_path}")

print("\n" + "=" * 55)
print("FIGURE GENERATION COMPLETE")
print("=" * 55)
any_missing = False
for name in EXPECTED:
    st = status_for(name)
    icon = '✓' if st == 'OK' else '✗'
    if st != 'OK': any_missing = True
    print(f"  {icon} {name}: {st}")
print(f"\nAll files saved to: {FIG_DIR}")

if any_missing:
    raise SystemExit(1)
